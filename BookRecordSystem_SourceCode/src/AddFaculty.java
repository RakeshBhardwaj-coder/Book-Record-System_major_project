    private void AddBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddBtnActionPerformed
        // TODO add your handling code here:
        String facName, facGender = "", facEmail, facDetail, facAddress, facSeason;
        long facMobile;
        facName = this.facName.getText();
        if (facMale.isSelected()) {
            facGender = "Male";
        } else if (facFemale.isSelected()) {
            facGender = "Female";
        } else if (facOther.isSelected()) {
            facGender = "Other";
        }
        facEmail = this.facEmail.getText();
        facMobile = Long.parseLong(this.facMobile.getText());
        facDetail = this.facDetail.getText();
        facAddress = this.facAddress.getText();
        facSeason = this.facSeason.getText();

        Connection con;
        Statement stmt;
        String qry;

        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_Book_Record_System", "root", "");

            qry = "INSERT INTO db_Faculty(fac_name,fac_gender,fac_email,fac_mobile,fac_detail,fac_address,fac_season) VALUES('" + facName + "', '" + facGender + "', '" + facEmail + "', " + facMobile + ",'" + facDetail + "' , '" + facAddress + "', '" + facSeason + "')";
            stmt = con.createStatement();
            int row = stmt.executeUpdate(qry);
            if (row > 0) {
                JOptionPane.showMessageDialog(this, "Data Added");
                LoadFacultyData();
            } else {
                JOptionPane.showMessageDialog(this, "Sorry data not Added");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_AddBtnActionPerformed

