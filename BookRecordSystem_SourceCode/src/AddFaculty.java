    public void LoadFacultyData() {

        Connection con;
        Statement stmt;
        ResultSet res;
        String qry;
        DefaultTableModel dtm;
        int sno = 1;

        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_Book_Record_System", "root", "");
            stmt = con.createStatement();
            qry = "Select * from db_Faculty";
            res = stmt.executeQuery(qry);

            Object[] facRecords = new Object[9];

            dtm = (DefaultTableModel) facRecordsTable.getModel();
            dtm.setRowCount(0);

            while (res.next()) {
                facRecords[0] = sno;
                facRecords[1] = res.getInt("fac_id");
                facRecords[2] = res.getString("fac_name");
                facRecords[3] = res.getString("fac_gender");
                facRecords[4] = res.getString("fac_email");
                facRecords[5] = res.getLong("fac_mobile");
                facRecords[6] = res.getString("fac_detail");
                facRecords[7] = res.getString("fac_address");
                facRecords[8] = res.getString("fac_season");
                dtm.addRow(facRecords);
                sno++;

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }

