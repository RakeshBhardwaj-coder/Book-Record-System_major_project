    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here:

        Connection con;
        Statement stmt;
        String qry;

        int delete = JOptionPane.showConfirmDialog(this,
                "Do you really want to delete?", "Select an Option...", JOptionPane.YES_NO_CANCEL_OPTION);
        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_Book_Record_System", "root", "");
            stmt = con.createStatement();
            if (delete == 0) {
                // 0:yes 1:No 2:cancel
                qry = "Delete From db_Instructor where ins_id=" + Integer.parseInt(insId.getText());
                int row = stmt.executeUpdate(qry);
                if (row > 0) {
                    LoadInstructorData();
                } else {
                    JOptionPane.showMessageDialog(this, "Sorry not Deletedted.");
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_deleteActionPerformed

