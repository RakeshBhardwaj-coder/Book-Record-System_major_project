    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:

        String insName, insGender = "", insEmail, insPass, insDetail, insAddress, insSeason;
        long insMobile;
        insName = this.insName.getText();
        if (insMale.isSelected()) {
            insGender = "Male";
        } else if (insFemale.isSelected()) {
            insGender = "Female";
        } else if (insOther.isSelected()) {
            insGender = "Other";
        }
        insEmail = this.insEmail.getText();
//        insPass = this.insPass.getText();
        insMobile = Long.parseLong(this.insMobile.getText());
        insDetail = this.insDetail.getText();
        insAddress = this.insAddress.getText();

        Connection con;
        Statement stmt;
        String qry;

        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_Book_Record_System", "root", "");
            stmt = con.createStatement();
            qry = "UPDATE db_Instructor SET ins_name = '" + insName + "', ins_gender='" + insGender + "', ins_email='" + insEmail + "', ins_mobile=" + insMobile + " , ins_detail='" + insDetail + "', ins_address='" + insAddress + "' WHERE ins_id = " + Integer.parseInt(insId.getText());

            int row = stmt.executeUpdate(qry);
            if (row > 0) {
                JOptionPane.showMessageDialog(this, "Update Succssesfully");
                LoadInstructorData();
            } else {
                JOptionPane.showMessageDialog(this, "Sorry not Updated.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_updateBtnActionPerformed

