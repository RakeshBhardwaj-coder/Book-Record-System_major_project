    public void LoadInstructorData() {

        Connection con;
        Statement stmt;
        ResultSet res;
        String qry;
        DefaultTableModel dtm;
        int sno = 1;

        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_Book_Record_System", "root", "");
            stmt = con.createStatement();
            qry = "Select * from db_Instructor";
            res = stmt.executeQuery(qry);

            Object[] insRecords = new Object[8];

            dtm = (DefaultTableModel) insRecordsTable.getModel();
            dtm.setRowCount(0);

            while (res.next()) {
                insRecords[0] = sno;
                insRecords[1] = res.getInt("ins_id");
                insRecords[2] = res.getString("ins_name");
                insRecords[3] = res.getString("ins_gender");
                insRecords[4] = res.getString("ins_email");
                insRecords[5] = res.getLong("ins_mobile");
                insRecords[6] = res.getString("ins_detail");
                insRecords[7] = res.getString("ins_address");

                dtm.addRow(insRecords);
                sno++;

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }

