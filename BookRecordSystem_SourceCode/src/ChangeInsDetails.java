    private void ins_txt_searchCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_ins_txt_searchCaretUpdate
        // TODO add your handling code here:
        Connection con;
        Statement stmt;
        ResultSet res;
        String qry;
        DefaultTableModel dtm;
        int sno = 1;

        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_Book_Record_System", "root", "");
            stmt = con.createStatement();
            qry = "Select * from db_Instructor where ins_name like '%" + ins_txt_search.getText() + "%'";
            res = stmt.executeQuery(qry);

            Object[] insRecords = new Object[9];

            dtm = (DefaultTableModel) insRecordsTable.getModel();
            dtm.setRowCount(0);

            while (res.next()) {
                insRecords[0] = sno;
                insRecords[1] = res.getInt("ins_id");
                insRecords[2] = res.getString("ins_name");
                insRecords[3] = res.getString("ins_gender");
                insRecords[4] = res.getString("ins_email");
                insRecords[5] = res.getLong("ins_mobile");
                insRecords[6] = res.getString("ins_detail");
                insRecords[7] = res.getString("ins_address");
                //
                dtm.addRow(insRecords);
                sno++;
                //                System.out.println(num);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_ins_txt_searchCaretUpdate

